    /// <summary>
    /// Generates the mesh data to make a 1m sized cylinder mesh. Anchor point in in the bottom center of the mesh.
    /// </summary>
    /// <param name="points">The number of points the object has.</param>
    /// <returns></returns>
    public static Mesh MakeCylinder(int points) {
        List<Vector3> verts = new List<Vector3>();
        //List<Vector2> uvs = new List<Vector2>();
        List<Vector3> normals = new List<Vector3>();
        List<int> tris = new List<int>();

        //Generate vertices based on number of points & set normals:
        //Set start points in middle:
        verts.Add(new Vector3(0, 0, 0));                //verts(0) on Bottom Center
        normals.Add(new Vector3(0, -1, 0));                          //normals for verts(0)

        verts.Add(new Vector3(0, 1, 0));                //verts(1) on Top Center
        normals.Add(new Vector3(0, +1, 0));              //normals for verts(1)

        //Get points for all sides(bottom, then top); the sides will start at verts(2):
        for (int i = 0; i < points; i++) {
            //find angle to spawn verts at:
            float angleDegrees = (i == 0) ? 0 : 360 / ((float)points / (float)i);
            float angleRadians = angleDegrees * (Mathf.PI / 180);
            float radius = .5f;

            //find x and z positions of created vertices:
            float vertX = Mathf.Sin(angleRadians) * radius;
            float vertZ = Mathf.Cos(angleRadians) * radius;

            //set bottom vert & normals
            verts.Add(new Vector3(vertX, 0, vertZ));
            int point1Bottom = verts.Count - 1;
            normals.Add(Vector3.Normalize(verts[point1Bottom] - verts[0]));
            //set top vert & normals
            verts.Add(new Vector3(vertX, 1, vertZ));
            int point1Top = verts.Count - 1;
            normals.Add(Vector3.Normalize(verts[point1Top] - verts[1]));
        }

        //TODO: Generate UV values:

        //Generate triangles based on the created vertices
        //Side Trianges:
        //set up left and right index values:
        int leftBottomIndex = 2;
        int leftTopIndex = 3;

        int rightBottomIndex;
        int rightTopIndex;

        for (int i = 1; i <= points; i++) {
            if (i != points) {
                //set index values for the right vertices:
                rightBottomIndex = leftBottomIndex + 2;
                rightTopIndex = leftTopIndex + 2;


            } else {
                //set right vertices to loop back to the starting side vertices:
                rightBottomIndex = 2;
                rightTopIndex = 3;
            }

            //create triangles:
            tris.Add(leftBottomIndex);
            tris.Add(rightTopIndex);
            tris.Add(leftTopIndex);

            tris.Add(rightTopIndex);
            tris.Add(leftBottomIndex);
            tris.Add(rightBottomIndex);

            //Set index values for next side.
            leftBottomIndex = rightBottomIndex;
            leftTopIndex = rightTopIndex;
        }

        //Bottom Triangles (Even index values):
        for (int bv = 2; bv <= verts.Count - 2; bv += 2) {
            if (bv != verts.Count - 2) {
                //grab vertices from index
                //start tri at bv (Bottom Vertex)
                tris.Add(bv);
                //Set vertex at index 0
                tris.Add(0);
                //End at vertex bv + 2;
                tris.Add(bv + 2);
            } else {
                //Set last triangle using the center and point 1:
                tris.Add(bv);
                tris.Add(0);            //Bottom Center Index
                tris.Add(2);            //Bottom Point 1 Index
            }
        }

        //Top Triangles (Odd index values):
        for (int tv = 1; tv <= verts.Count - 1; tv += 2) {
            if (tv != verts.Count - 1) {
                //set tris from vertices at index
                //Set at vertex tv + 2
                tris.Add(tv + 2);
                //Set at vertex at index 1
                tris.Add(1);
                //start tri at tv (Top Vertex)
                tris.Add(tv);
            } else {
                //Set last triangle using the center and point 1:
                tris.Add(3);
                tris.Add(1);                //Top Center Index
                tris.Add(tv);                //Top Point 1 Index
            }
        }

        //Generate mesh based on verts, tris, and normals
        Mesh mesh = new Mesh();
        mesh.SetVertices(verts);
        //mesh.SetUVs(uvs);
        mesh.SetNormals(normals);
        mesh.SetTriangles(tris, 0);
        return mesh;
    }
    public static Mesh FromTris(List<Triangle> tris){

        List<Vector3> vertexList = new List<Vector3>();
        List<int> triangleList = new List<int>();
        List<Vector3> normals = new List<Vector3>();
        //List<FProcMeshTangent> tangents;
        List<Vector2> uvs = new List<Vector2>();
        List<Vector3> colors = new List<Vector3>();

        for (int i = 0; i < tris.Count; i++) {
            triangleList.Add(vertexList.Count);
            vertexList.Add(tris[i].a);
            triangleList.Add(vertexList.Count);
            vertexList.Add(tris[i].b);
            triangleList.Add(vertexList.Count);
            vertexList.Add(tris[i].c);
            
            Vector3 n = tris[i].CalcNormal();
            normals.Add(n);
            normals.Add(n);
            normals.Add(n);
        }

        //normals.SetNum(vertexList.Num());
        //tangents.SetNum(vertexList.Num());

        // submit mesh data to component:
        
        Mesh mesh = new Mesh();
        mesh.vertices = vertexList.ToArray();
        mesh.triangles = triangleList.ToArray();
        mesh.normals = normals.ToArray();
        mesh.uv = uvs.ToArray();
        return mesh;
    }

    public class CubeMarcher {
        delegate float DensityLookup(int x, int y, int z);
        public static async Task<Mesh> MakeMeshAsync(float voxelSize, float iso, float[,,] densities){
            List<Triangle> tris = new List<Triangle>();

            Vector3[] positions = { 
                voxelSize * new Vector3(-0.5f, -0.5f, +0.5f), // L B B
                voxelSize * new Vector3(+0.5f, -0.5f, +0.5f), // R B B
                voxelSize * new Vector3(+0.5f, -0.5f, -0.5f), // R B F
                voxelSize * new Vector3(-0.5f, -0.5f, -0.5f), // L B F
                voxelSize * new Vector3(-0.5f, +0.5f, +0.5f), // L T B
                voxelSize * new Vector3(+0.5f, +0.5f, +0.5f), // R T B
                voxelSize * new Vector3(+0.5f, +0.5f, -0.5f), // R T F
                voxelSize * new Vector3(-0.5f, +0.5f, -0.5f)  // L T F
            };
            DensityLookup Lookup = (int x, int y, int z) => {
                if (x < 0 || y < 0 || z < 0) return 0;
                if (x >= densities.GetLength(0)) return 0;
                if (y >= densities.GetLength(1)) return 0;
                if (z >= densities.GetLength(2)) return 0;

                return densities[x, y, z];
            };

            int steps = 10;
            int chunks_per_frame = densities.GetLength(2) / steps;

            for (int z = 0; z < densities.GetLength(2); z++) {
                for (int x = 0; x < densities.GetLength(0); x++) {
                    for (int y = 0; y < densities.GetLength(1); y++) {

                        float[] local_densities = {
                            Lookup(x + 0, y + 0, z + 1),
                            Lookup(x + 1, y + 0, z + 1),
                            Lookup(x + 1, y + 0, z + 0),
                            Lookup(x + 0, y + 0, z + 0),
                            Lookup(x + 0, y + 1, z + 1),
                            Lookup(x + 1, y + 1, z + 1),
                            Lookup(x + 1, y + 1, z + 0),
                            Lookup(x + 0, y + 1, z + 0)
                        };

                        int bitfield = 0;
                        if (local_densities[0] < iso) bitfield |= 1;
                        if (local_densities[1] < iso) bitfield |= 2;
                        if (local_densities[2] < iso) bitfield |= 4;
                        if (local_densities[3] < iso) bitfield |= 8;
                        if (local_densities[4] < iso) bitfield |= 16;
                        if (local_densities[5] < iso) bitfield |= 32;
                        if (local_densities[6] < iso) bitfield |= 64;
                        if (local_densities[7] < iso) bitfield |= 128;

                        // Cube is entirely in/out of the surface 
                        if (EdgeTable[bitfield] == 0) continue; // skip to next voxel

                        // Find the vertices where the surface intersects the cube 
                        Vector3[] vertlist = new Vector3[12];
                        if ((EdgeTable[bitfield] &    1) > 0) vertlist[0]  = LerpEdge(iso, positions[0], positions[1], local_densities[0], local_densities[1]);
                        if ((EdgeTable[bitfield] &    2) > 0) vertlist[1]  = LerpEdge(iso, positions[1], positions[2], local_densities[1], local_densities[2]);
                        if ((EdgeTable[bitfield] &    4) > 0) vertlist[2]  = LerpEdge(iso, positions[2], positions[3], local_densities[2], local_densities[3]);
                        if ((EdgeTable[bitfield] &    8) > 0) vertlist[3]  = LerpEdge(iso, positions[3], positions[0], local_densities[3], local_densities[0]);
                        if ((EdgeTable[bitfield] &   16) > 0) vertlist[4]  = LerpEdge(iso, positions[4], positions[5], local_densities[4], local_densities[5]);
                        if ((EdgeTable[bitfield] &   32) > 0) vertlist[5]  = LerpEdge(iso, positions[5], positions[6], local_densities[5], local_densities[6]);
                        if ((EdgeTable[bitfield] &   64) > 0) vertlist[6]  = LerpEdge(iso, positions[6], positions[7], local_densities[6], local_densities[7]);
                        if ((EdgeTable[bitfield] &  128) > 0) vertlist[7]  = LerpEdge(iso, positions[7], positions[4], local_densities[7], local_densities[4]);
                        if ((EdgeTable[bitfield] &  256) > 0) vertlist[8]  = LerpEdge(iso, positions[0], positions[4], local_densities[0], local_densities[4]);
                        if ((EdgeTable[bitfield] &  512) > 0) vertlist[9]  = LerpEdge(iso, positions[1], positions[5], local_densities[1], local_densities[5]);
                        if ((EdgeTable[bitfield] & 1024) > 0) vertlist[10] = LerpEdge(iso, positions[2], positions[6], local_densities[2], local_densities[6]);
                        if ((EdgeTable[bitfield] & 2048) > 0) vertlist[11] = LerpEdge(iso, positions[3], positions[7], local_densities[3], local_densities[7]);

                        // find center of voxel:
                        Vector3 center = new Vector3(x, y, z) * voxelSize;
                        for (int i = 0; TriTable[bitfield,i] != -1; i += 3)
                        {
                            Vector3 a = vertlist[TriTable[bitfield, i + 0]] + center;
                            Vector3 b = vertlist[TriTable[bitfield, i + 1]] + center;
                            Vector3 c = vertlist[TriTable[bitfield, i + 2]] + center;

                            tris.Add(new Triangle(a, b, c));
                        }

                    } // end of y
                } // end of x
                if(z % chunks_per_frame == 0) await Task.Yield();
            } // end of z

            // Debug.Log($"cubes marched! {tris.Count} triangles");

            return MeshTools.FromTris(tris);
        }

        static Vector3 LerpEdge(float iso, Vector3 p1, Vector3 p2, float val1, float val2){
            const float threshold = 0.00001f;

            if (Mathf.Abs(iso - val1) < threshold) return p1; // if p1 is very close to the threshold, just return p1
            if (Mathf.Abs(iso - val2) < threshold) return p2; // if p2 is very close to the threshold, just return p2
            if (Mathf.Abs(val1 - val2) < threshold) return p1; // if val1 and val2 are (almost) the same density
            float percent = (iso - val1) / (val2 - val1);

            return Vector3.Lerp(p1, p2, percent);
        }
